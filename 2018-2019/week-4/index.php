			<h3 class="unstack">Tips for Working with Developers</h3>
			<ul>
				<li><strong>Do's</strong>
					<ul>
						<li>Have <em>everything</em> as buttoned up as humanly possible</li>
						<li>Make interactive prototypes that show exactly what you want done</li>
						<li>Provide detailed text annotations for difficult to understand concepts or interactions</li>
						<li>Define, from the start, which platforms your app is expected to work on:
							<ul>
								<li>Web browsers: Chrome, Firefox, Safari</li>
								<li>Operating Systems: Mac, Windows</li>
								<li>Devices: Mobile, Tablet, Desktop, Desktop HD</li>
								<li>iOS: which iPhone models, which Android models</li>
							</ul>
						<li><strong>ASSUME NOTHING!</strong></li>
						<li>If you don't specify exactly what you want, you're not likely to get it</li>
						<li>If you don't specify platforms, developers will not test on those platforms</li>
						<li>Do use a tool like <a href='http://zeplin.io' target='_blank' >Zeplin</a> to relay designs to developers</li>
					</ul>
				</li>
				<li><strong>Dont's</strong>
					<ul>
						<li>Don't have a text-only relationship. Do schedule weekly calls.</li>
						<li>Don't assume off-shoring is automatically a bad idea, though if there's a language barrier, be prepared to be extra vigilant. In these cases, it's sometimes better to send videos rather than text emails</li>
						<li>Make sure that you're using a ticketing system (<a href='https://about.gitlab.com/' target='_blank' >Gitlab</a>, <a href='https://www.atlassian.com/software/jira/features' target='_blank' >Jira</a>, <a href='https://www.pivotaltracker.com' target='_blank' >Pivotal Tracker</a>) to log bug reports and track progress</li>						
					</ul>
				</li>
			</ul>

			
